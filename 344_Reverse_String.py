## Problem Statement ##
'''
Write a function that takes a string as input and returns the string reversed.

Example:
Given s = "hello", return "olleh".
'''

## My Python3 code ##

class Solution:
    def reverseString(self, s):
        return s[::-1]
        """
        :type s: str
        :rtype: str
        """

